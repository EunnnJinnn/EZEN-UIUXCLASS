// 수학객체
// Math

let num = 2.9234;

let maxNum = Math.max(10, 5, 8, 30);
console.log(maxNum);

let minNum = Math.min(10, 5, 8, 30);
console.log(minNum);

let roundNum = Math.round(num); //5 이상일 경우 반올림
console.log(roundNum);

let floorNum = Math.floor(num); // 소수점 버림
console.log(floorNum);

let ceilNum = Math.ceil(num); // 소수점 살림
console.log(ceilNum);

let rndNum = Math.random(); //소수점을 가지고 있는 정수를 난수로 변경
console.log(rndNum);

let piNum = Math.PI;
console.log(piNum);
